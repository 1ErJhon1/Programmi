    private void btncercaActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btncercaActionPerformed
        for(int i=0;i<calciatori.size();i++){
            if(calciatori.get(i).getNome().equals(txtcercacalc.getText())){
                jTextArea1.setText(calciatori.get(i).toString());
            }
        }
        for(int i=0;i<portieri.size();i++){
            if(portieri.get(i).getNome().equals(txtcercacalc.getText())){
                jTextArea1.setText(portieri.get(i).toString());
            }
        }
    }//GEN-LAST:event_btncercaActionPerformed

    private void btngiocpartActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btngiocpartActionPerformed
        int max=0,index=-1,tipo=-1;
        boolean check=false;
        for(int i=0;i<calciatori.size();i++){
            if(calciatori.get(i).getPartiteGiocate()>max){
                max=calciatori.get(i).getPartiteGiocate();
                index=i;
                tipo=0;
                check=true;
            }
        }
        for(int i=0;i<portieri.size();i++){
            if(portieri.get(i).getPartiteGiocate()>max){
                max=portieri.get(i).getPartiteGiocate();
                index=i;
                tipo=1;
                check=true;
            }
        }
        if(tipo==0){
            jTextArea1.setText(calciatori.get(index).toString());
        }
        if(tipo==1){
            jTextArea1.setText(portieri.get(index).toString());
        }
        if(check==false){
            jTextArea1.setText("Non c'Ã¨ nessun calciatore o portiere con quel nome");
        }
    }//GEN-LAST:event_btngiocpartActionPerformed

