    private void btninsActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btninsActionPerformed
        String nome=JOptionPane.showInputDialog("Inserisci il nome dell'atleta");
        String nazionalita=JOptionPane.showInputDialog("Inserisci la nazionalita'");
        int numeroGare=Integer.valueOf(JOptionPane.showInputDialog("Inserisci il numero di gare"));
        int annoNascita=Integer.valueOf(JOptionPane.showInputDialog("Inserisci l'anno di nascita"));
        Object[] opzioni = {"Nuotatore", "Pallavolista"};
        Object selezione = JOptionPane.showInputDialog(null, "Seleziona il tipo di atleta:", "Tipo", JOptionPane.INFORMATION_MESSAGE, null, opzioni, opzioni[0]);
        if(selezione.equals("Nuotatore")){
            String stile=JOptionPane.showInputDialog("Inserisci lo stile preferito");
            int tempo100=Integer.valueOf(JOptionPane.showInputDialog("Inserisci il tempo record sui 100 metri in secondi"));
            int tempo200=Integer.valueOf(JOptionPane.showInputDialog("Inserisci il tempo record sui 100 metri in secondi"));
            Nuotatore nuotatore=new Nuotatore(stile, tempo100, tempo200, nome, nazionalita, numeroGare, annoNascita);
            nuotatori.add(nuotatore);
        }else{
            int falli=Integer.valueOf(JOptionPane.showInputDialog("Inserisci il numero di falli"));
            int vinte=Integer.valueOf(JOptionPane.showInputDialog("Inserisci il numero di partite vinte"));
            int perse=Integer.valueOf(JOptionPane.showInputDialog("Inserisci il numero di partite perse"));
            Pallavolista pallavolista=new Pallavolista(falli, vinte, perse, nome, nazionalita, numeroGare, annoNascita);
            Object[] row={nome,nazionalita,numeroGare,annoNascita,"/","/","/",falli,vinte,perse};
            dtm.addRow(row);
            pallavolisti.add(pallavolista);
        }
        for(int i=0;i<dcbm.getSize();i++){
            if(!dcbm.getElementAt(i).toString().equals(nazionalita)) {
                dcbm.addElement(nazionalita);
            }
        }
    }//GEN-LAST:event_btninsActionPerformed

