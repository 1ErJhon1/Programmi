    private void jButton1ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton1ActionPerformed
        int pos=0;
        int scarto=pallavolisti.getFirst().vinte-pallavolisti.getFirst().perse;
        for(int i=0;i<pallavolisti.size();i++){
            if((pallavolisti.get(i).vinte-pallavolisti.get(i).perse)<scarto){
                pos=i;
                scarto=pallavolisti.get(i).vinte-pallavolisti.get(i).perse;
            }
        }
        txtarea.setText("Lo scarto minore tra partite vine e partite perse ce l'ha "+pallavolisti.get(pos).nome+"\n"+pallavolisti.get(pos).toString());
    }//GEN-LAST:event_jButton1ActionPerformed

    private void jButton2ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton2ActionPerformed
        int num=0,s=0;
        for(int i=0;i<nuotatori.size();i++){
            s+=nuotatori.get(i).eta(nuotatori.get(i).annoNascita);
            num++;
        }
        for(int i=0;i<pallavolisti.size();i++){
            s+=pallavolisti.get(i).eta(pallavolisti.get(i).annoNascita);
            num++;
        }
        txtarea.setText("l'eta' media tra nuotatori e pallavolisti e': "+(s/num));
    }//GEN-LAST:event_jButton2ActionPerformed

